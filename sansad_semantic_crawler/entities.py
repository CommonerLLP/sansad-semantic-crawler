"""Entity store: stable identity for politicians, ministers, bureaucrats.

The entity record holds **only what's needed to identify a person**:
canonical name, alt-names, a stable id, and a primary kind. No biography,
no externally-sourced data. Every future enrichment (cabinet portfolio
history, ECI nominations, asset declarations, criminal records, DoPT
postings) is a **sidecar JSONL file** that joins on ``entity_id``.

Schema commitments (see ``entities/README.md`` written by ``save()``):

* ``people.jsonl`` — minimal identity records, schema-stable forever.
* ``mp_memberships.jsonl`` — temporal MP role data (one row per
  (entity_id, house, term)).
* ``committee_memberships.jsonl`` — temporal committee role data.
* ``ministerial_appointments.jsonl`` — temporal cabinet/MoS role data.
* ``bureaucratic_postings.jsonl`` — temporal IAS/civil-service role
  data, populated organically as bureaucrats are encountered in PDFs.

Reserved file names (schema committed, no implementation yet):

* ``eci_nominations.jsonl`` — Election Commission nomination records.
* ``asset_declarations.jsonl`` — wealth disclosures from nominations.
* ``criminal_records.jsonl`` — pending cases from nominations.
* ``bureaucratic_external.jsonl`` — DoPT cadre/batch/posting data.
* ``education.jsonl``, ``prior_career.jsonl`` — biographical sidecars.

The reserved-namespace policy means a future contributor knows the
file name in advance; nothing is named ad-hoc.

An optional external-priors layer (when activated) lives outside the
corpus directory. Entity records are the public identity layer; any
qualitative priors are kept separate.
"""

from __future__ import annotations

import hashlib
import json
import re
from dataclasses import asdict, dataclass, field
from datetime import datetime
from pathlib import Path
from typing import Iterable

